function ArrayNumber (Number) { //ประกาศฟังก์ชันชื่อ ArrayNumber และ รับค่า Number เข้าไป
    let sum = 0; //ประกาศชนิดตัวแปรเป็น let ชื่อ sum เพื่อไว้เปลี่ยนค่าsum
    for (let i = 0; i < Number.length; i++) { /* for คือ การวนลูป let i = 0 ประกาศตัวแปร i ให้ = 0 
        i < Number.length คือ ตรวจสอบว่า i น้อยกว่าจำนวนสมาชิกใน Array หรือยัง
        i++ คือเพิ่มค่า i ทีละ 1 ถ้ายังน้อยกว่าสมาชิกใน Array ให้ทำการเพิ่ม 1 ไปเรื่อยๆ 
        เช่น i = 0 และ i < จำนวนสมาชิกในArray i = 1 เพิ่มไปเรื่อยๆจนกว่าจะ = 5 หรือ มากกว่าเท่ากับ 5 เพื่อหยุดการลูป  */
        sum += Number[i] /*คือการเพิ่มค่า Number[i] ไปยัง sum โดย [i] คือตำแหน่งแรกของ sum คือตำแหน่งที่ 0
        และตำแหน่งที่ 0 คือ 5 จากนั้น ก็เพิ่มค่า i ไปเรื่อยๆจนถึงตำแหน่งที่ 5 จากนั้นค่า sum+= Number ก็จะ =
        5 + 6 + 7 + 8 + 9 + 10 = 45*/ 
    }
    return sum / Number.length; // sum ที่ได้ทั้งหมด คือ 45 มาหาร จำนวนสมาชิกอาเรย์คือ 6
}

console.log(ArrayNumber([5, 6, 7, 8, 9, 10])) //จำนวนสมาชิกในอาเรย์นี้ มี 6

